package lab_2.api;

/**
 * Інтерфейс черги задач із відстеженням сумарного часу виконання.
 */
public interface TaskQueue {
    
    /**
     * Додає задачу в чергу та оновлює загальний час навантаження.
     * @param task об'єкт задачі.
     */
    void add(Task task);

    /**
     * Вилучає задачу з черги та віднімає її час від загального навантаження.
     * @return задача або null, якщо черга порожня.
     */
    Task pop();

    /**
     * Повертає сумарний час виконання всіх задач, що наразі знаходяться в черзі.
     * @return загальна тривалість у секундах.
     */
    long getTotalDuration();

    /**
     * Повністю очищує чергу та скидає лічильник часу.
     */
    void clear();
}
