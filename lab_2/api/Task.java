package lab_2.api;

/**
 * Інтерфейс задачі, що містить інформацію про час її виконання.
 */
public interface Task {

    /**
     * Виконує основну логіку задачі.
     */
    void execute();

    /**
     * Повертає очікувану тривалість виконання задачі у секундах.
     * @return ціле число від 5 до 20.
     */
    int getDurationSeconds();
}
